import {useState} from "react";

let i = 0
const getI = () => (i++).toString()

const Directory = ({nodeId, name, children}) => (
	<TreeItem className={'directory'} nodeId={nodeId} label={name}>
		{
			children?.map(c => c.type === 'directory' ?
			<Directory key={getI()} nodeId={getI()} name={c.name} children={c.contents}/> :
			<TreeItem key={getI()} nodeId={getI()} label={c.name}/>)
		}
	</TreeItem>
);


const App = () => {
	const [files] = useState(data)

	var root = files.find(f => f.type === 'directory')
	var rootDescriptor = files.find(f => f.type === 'report')

	return (
		<div className={"explorer"}>
			<div className={"root-directory-descriptor"}>
				<span>{"Directories: " + rootDescriptor.directories}</span>
				<span>{"Files: " + rootDescriptor.files}</span>
			</div>
			<TreeView
				defaultExpandIcon={<ArrowRightSvg/>}
				defaultCollapseIcon={<ArrowDownSvg/>}>
				<Directory nodeId={getI()} name={root.name} children={root.contents}/>
			</TreeView>
		</div>
	);
}


